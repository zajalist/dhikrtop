/** Pick a random adhkar from the given categories. */
export function pickRandom(categories: Adhkar['category'][]): Adhkar {
  const pool = ADHKAR.filter((a) => categories.includes(a.category));
  const source = pool.length > 0 ? pool : ADHKAR;
  return source[Math.floor(Math.random() * source.length)];
}
